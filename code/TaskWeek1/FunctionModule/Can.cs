        public static UInt32 m_devtype = 3;//USBCAN2

        public static UInt32 m_devind = 0;
        public static UInt32 m_canind = 0;



        public static void CANConnectAndStart(Timer timer1)//打开和连接CAN
        {
            m_devtype = 3;
            m_devind = 0;
            m_canind = 0;
            //VCI_OpenDevice(m_devtype, m_devind, 0);
            if (VCI_OpenDevice(m_devtype, m_devind, 0) == 0)
            {
                MessageBox.Show("打开设备失败,请检查设备是否连接正确", "错误",
                        MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
                return;
            }
            VCI_INIT_CONFIG config = new VCI_INIT_CONFIG();

            config.AccCode = 0x00000000;
            config.AccMask = 0xFFFFFFFF;
            config.Timing0 = 0x00;
            config.Timing1 = 0x1C;
            config.Filter = 1;
            config.Mode = 0;
            VCI_InitCAN(m_devtype, m_devind, m_canind, ref config);
            timer1.Enabled = true;
            VCI_StartCAN(m_devtype, m_devind, m_canind);
        }
    }
